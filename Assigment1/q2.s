AREA    largeno,CODE,READONLY
    EXPORT __main
    ENTRY
__main    FUNCTION
		MOV R0, #12		;FIRST NUMBER
		MOV R1, #12	    ;SECOND NUMBER
		MOV R2, #20		;THIRD NUMBER
		MOV R4, #0		;INITIALIZING REGISTER WHERE THE MAXIMUM VALUE WILLBE STORED
		
		CMP R0, R1		;COMPARING THE FIRST TWO NUMBERS
		BGE LOOP1		;IF R0 IS GREATER, THEN BRANCH TO LOOP1
		BLT LOOP2		;IF R0 IS LESSER, THEN BRANCH TO LOOP2
		
COMP2	CMP R4, R2		;COMPARING MAXIMUM OF FIRST TWO NUMBERS WITH THE THIRD NUMBER
		BGE stop		;IF R4 IS GREATER, THEN BRANCH TO stop
		BLT LOOP3		;IF R4 IS LESSER, THEN BRANCH TO LOOP3

LOOP1 	MOV R4, R0		;SAVING THE MAXIMUM VALUE
		B COMP2			;BRANCHING BACK TO COMP2

LOOP2 	MOV R4, R1		;SAVING THE MAXIMUM VALUE
		B COMP2			;BRANCHING BACK TO COMP2
		
LOOP3 	MOV R4, R2 		;SAVING THE MAXIMUM VALUE
		B stop			;BRANCHING TO stop

stop    B stop			;LOOP TO STOP THE EXECUTION
		ENDFUNC
		END

;THIS CODE WILL GIVE THE MAXIMUM OF THREE NUMBERS.
;THE MAXIMUM WILL BE STORED IN REGISTER, R4